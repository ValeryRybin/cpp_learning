#include <iostream> // Подключение библиотеки для ввода/вывода

int main() {
    std::cout << "Hello world!" << std::endl; // Вывод строки "Привет, мир!" и переход на новую строку
    int number = 20;
    std::cout << "My variable: " << number << "\n"; // Вывод текста и значения переменной, переход на новую строку

    return 0; // Индикатор успешного завершения программы
}